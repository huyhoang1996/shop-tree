package com.softech.shop.view_model;

/**
 *
 * @author PC
 */
public class CustomerViewModel {
    private String email,customerName,password,reNewPassword,phone,address;

    public CustomerViewModel() {
        
    }

    public CustomerViewModel(String email, String customerName, String password, String reNewPassword, String phone, String address) {
        this.email = email;
        this.customerName = customerName;
        this.password = password;
        this.reNewPassword = reNewPassword;
        this.phone = phone;
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getReNewPassword() {
        return reNewPassword;
    }

    public void setReNewPassword(String reNewPassword) {
        this.reNewPassword = reNewPassword;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    
}
